`timescale 1ns / 1ps
//////////////////////////////////////////////////////////////////////////////////
// Description: 1位加法器，是32位cla最底层的模块
//              输入：两个1位数据a,b和一个低位进位c
//              输出：1位加法结果s，进位产生函数g，进位传递函数p
//////////////////////////////////////////////////////////////////////////////////


module add_1 (a,b,c, g,p,s);
    input a,b,c;
    output g,p,s;
    assign s = a ^ b ^ c;
    assign g = a & b;
    assign p = a | b;
endmodule
